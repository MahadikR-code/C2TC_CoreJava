
public class switch_case {

	public static void main(String[] args) 
	{
	int x=100;
	
	switch(x)
	{
	default:
		System.out.println("default");
		break;
		
	case 0:
		System.out.println(0);
		break;
	case 1:
		System.out.println(1);
	case 2:
		System.out.println(2);		
	}
	
	}

}
