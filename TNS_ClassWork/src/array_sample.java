

public class array_sample {

	public static void main(String[] args) {
   
		//Array declaration
		int[][] x= new int[2][3];
//    System.out.println(x.getClass().getName());  //class of variable x.
		System.out.println(x);
		System.out.println(x[0]);
		System.out.println(x[0][0]);
	}

}

